//Print the characters corresponding to ASCII codes from 65 to 90 in a tabular format like 
//65-A
//66-B

public class JavaSession1Assignment1_4 {

	
public static void main (String[] args) 
     
     {
    	 
    	    char a ;

 		    for (int i = 65 ; i <= 90 ; i++)
 			
 	 {
 			
 			a= (char)i;
 			
 			System.out.println(i+ " - "+a);
 			
 			
 	 }
 		    
    	 
     }
	
	
}
